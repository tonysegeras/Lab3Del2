package TonyJocke_Lab3Del2;

public class Author {
	private String name;
	
	public Author(String name) {
		this.name = new String(name);
	}
	
	public String returnAuthor() {
		String info = this.name;
		return info;
	}
	
	public String toString() {
		String info = this.name;
		return info;
	}
}
